//  Pernyataan if else bertingkat (nested if-else)
let nilai = 76;

if (nilai >= 90) {
  console.log("Nilai A");
} else if (nilai >= 80) {
  console.log("Nilai B");
} else if (nilai >= 70) {
  console.log("Nilai C");
} else if (nilai >= 60) {
  console.log("Nilai D");
} else {
  console.log("Nilai E (Tidak Lulus)");
}